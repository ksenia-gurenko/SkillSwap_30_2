/**
 * Цвет по умолчанию для неизвестных навыков
 */
export const DEFAULT_COLOR = '#E8ECF7';

/**
 * Маппинг навыков к цветам (создается автоматически)
 */
export const SKILL_COLOR_MAP = Object.values(SKILL_CATEGORIES).reduce(
  (map, { color, skills }) => {
    skills.forEach((skill) => (map[skill] = color));
    return map;
  },
  {} as Record<string, string>
);

/**
 * Получение цвета навыка
 */
export const getSkillColor = (skillName: string): string =>
  SKILL_COLOR_MAP[skillName] || DEFAULT_COLOR;
